package POOExercicio2;

public class Agenda {
    // criar atributos
    String nome;
    int idade;
    double altura;
    // construtordefault (vazio)

    // criar sets and gets
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

}
